require File.expand_path('../../../../../spec_helper', __FILE__)

describe 'api/v2/planning_elements/destroy.api.rabl', type: :view do

  before do
    allow(view).to receive(:include_journals?).and_return(false)

    params[:format] = 'json'
  end

  let(:planning_element) { FactoryGirl.build(:work_package) }

  describe 'with an assigned planning element' do
    it 'renders a planning_element document' do
      assign(:planning_element, planning_element)

      render

      expect(response.body).to have_json_path('planning_element')
    end

  end
end
